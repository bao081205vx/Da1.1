private void loadHoaDonData() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien3.getModel();
    model.setRowCount(0);
    java.util.List<HoaDon> hoaDons = hoaDonService.getListHoaDon();
    int stt = 1;
    for (HoaDon hd : hoaDons) {
        model.addRow(new Object[]{
            stt++,
            hd.getMaHoaDon(),
            hd.getTenKhachHang(),
            hd.getTongTien() != null ? hd.getTongTien().toString() : "0.00",
            hd.getTrangThai(),
            hd.getNgayTao() != null ? hd.getNgayTao().toString() : ""
        });
    }
    }

