    private void cmdSearch3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSearch3ActionPerformed
        // TODO add your handling code here:
        String keyword = txtSearch3.getText().trim().toLowerCase();
    
    List<HoaDon> filteredList = new ArrayList<>();
    for (HoaDon hd : listKhoHang) {
        String idStr = String.valueOf(hd.getId());
        String tongTienStr = String.valueOf(hd.getTongTien());
        String ngayTaoStr = hd.getNgayTao() != null ? hd.getNgayTao().toString().toLowerCase() : "";
        if (idStr.contains(keyword) ||
            (hd.getMaHoaDon() != null && hd.getMaHoaDon().toLowerCase().contains(keyword)) ||
            (hd.getTenKhachHang() != null && hd.getTenKhachHang().toLowerCase().contains(keyword)) ||
            tongTienStr.contains(keyword) ||
            ngayTaoStr.contains(keyword)) {
            filteredList.add(hd);
        }
    }
    
    DefaultTableModel model = (DefaultTableModel) tblNhanVien3.getModel();
    model.setRowCount(0);
    
    for (HoaDon hd : filteredList) {
        Object[] row = {
            hd.getId(),
            hd.getMaHoaDon(),
            hd.getTenKhachHang(),
            hd.getNgayTao(),
            hd.getTongTien()
        };
        model.addRow(row);
    }
    
    if (filteredList.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy hóa đơn nào phù hợp với từ khóa: " + keyword, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_cmdSearch3ActionPerformed

